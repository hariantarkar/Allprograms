class player
{
	private int id;
	private String name;
	private int age;
	private int run;
public void setid(int id)
{
	this.id=id;
}
public int  getid()
{
	return id;
}
public void setName(String name)
{
	this.name=name;
}
public String getName()
{
	return name;
}
public void setage(int age)
{
	this.age=age;
}
public int getage()
{
	return age;
}
public void setrun(int run)
{
	this.run=run;
}
public int getrun()
{
	return run;
}
}






