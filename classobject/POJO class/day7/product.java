class product
{
	private int id;
	private String name;
	private int price;
public void setid(int id)
{
	this.id=id;
}
public int getid()
{
	return id;
}
public void setName(String name)
{
	this.name=name;
}
public String getName()
{
	return name;
}
public void setprice(int price)
{
	this.price=price;
}
public int getprice()
{
	return price;
}
}
